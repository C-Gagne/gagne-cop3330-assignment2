package org.example.ex37.Base;

import java.util.ArrayList;
import java.util.List;

public class ListOfLowerCase
{
    public Character getLowerCase(int randomNum)
    {
        List<Character> arrayOfLowerCase = new ArrayList<>();
        arrayOfLowerCase.add('a');
        arrayOfLowerCase.add('b');
        arrayOfLowerCase.add('c');
        arrayOfLowerCase.add('d');
        arrayOfLowerCase.add('e');
        arrayOfLowerCase.add('f');
        arrayOfLowerCase.add('g');
        arrayOfLowerCase.add('h');
        arrayOfLowerCase.add('i');
        arrayOfLowerCase.add('j');
        arrayOfLowerCase.add('k');
        arrayOfLowerCase.add('l');
        arrayOfLowerCase.add('m');
        arrayOfLowerCase.add('n');
        arrayOfLowerCase.add('o');
        arrayOfLowerCase.add('p');
        arrayOfLowerCase.add('q');
        arrayOfLowerCase.add('r');
        arrayOfLowerCase.add('s');
        arrayOfLowerCase.add('t');
        arrayOfLowerCase.add('u');
        arrayOfLowerCase.add('v');
        arrayOfLowerCase.add('w');
        arrayOfLowerCase.add('x');
        arrayOfLowerCase.add('y');
        arrayOfLowerCase.add('z');

        return arrayOfLowerCase.get(randomNum);
    }

}
